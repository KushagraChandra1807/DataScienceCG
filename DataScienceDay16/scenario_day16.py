# Scenario: Hospital FAQ Assistant
# A hospital wants to build a chatbot that helps patients quickly find answers to common questions.
# - Problem: Patients ask questions in different ways. For example:
# - “How do I book an appointment?”
# - “What’s the process to schedule a doctor visit?”
# - “Can I see a physician tomorrow?”
# Traditional keyword search might miss these connections.
# - Solution: Convert FAQs and patient queries into embeddings, then measure similarity to find the closest match.

# 👩‍💻 Code in Context

# Install first if needed:
# pip install sentence-transformers scikit-learn

from sentence_transformers import SentenceTransformer
from sklearn.metrics.pairwise import cosine_similarity

# Step 1: Load embedding model
model = SentenceTransformer('all-MiniLM-L6-v2')

# Step 2: Hospital FAQ database
faq_questions = [
    "How do I book an appointment?",
    "What are the hospital visiting hours?",
    "How can I contact a doctor?",
    "Where is the emergency department located?",
    "What documents are required for admission?"
]

faq_answers = [
    "You can book an appointment online through our hospital website or call the reception.",
    "Visiting hours are from 10 AM to 6 PM every day.",
    "You can contact a doctor through the hospital helpline or patient portal.",
    "The emergency department is located on the ground floor.",
    "You need an ID proof, insurance card, and previous medical records."
]

# Step 3: Convert FAQ questions to embeddings
faq_embeddings = model.encode(faq_questions)

# Step 4: Chatbot function
def hospital_chatbot(user_query):

    query_embedding = model.encode([user_query])

    # Compute similarity
    similarity = cosine_similarity(query_embedding, faq_embeddings)

    # Get best match
    best_match = similarity.argmax()

    return faq_answers[best_match]

# Step 5: User interaction
user_question = input("Ask your hospital question: ")

response = hospital_chatbot(user_question)

print("\nChatbot Answer:", response)

