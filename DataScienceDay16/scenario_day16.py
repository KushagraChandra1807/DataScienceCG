# Install required library
# pip install sentence-transformers

from sentence_transformers import SentenceTransformer
import numpy as np

# Step 1: Load embedding model
model = SentenceTransformer('all-MiniLM-L6-v2')

# Step 2: Training sentences (knowledge base)
sentences = [
    "Push-ups strengthen chest and triceps",
    "Yoga improves flexibility and balance",
    "Apples are rich in fiber",
    "Cardio exercises boost heart health"
]

# Step 3: Convert sentences to embeddings
embeddings = model.encode(sentences)

# Step 4: Function to find most similar sentence
def chatbot_response(query):

    query_embedding = model.encode([query])

    # Calculate similarity
    similarity = np.dot(embeddings, query_embedding.T)

    # Find best match
    index = np.argmax(similarity)

    return sentences[index]

# Step 5: Chatbot interaction
query = input("Ask a health question: ")

response = chatbot_response(query)

print("Chatbot Suggestion:", response)

# Scenario: Healthcare Chatbot – Patient Symptom Matching
# Imagine you’re designing a healthcare chatbot that helps patients find possible conditions based on their symptoms. Both patients and conditions are represented as vectors of features (e.g., fever, cough, fatigue, headache).
# - Patient’s symptom vector:
# [0.2,0.8,-0.3,0.5] → mild fever, strong cough, no fatigue, moderate headache.
