# Scenario: Fitness & Wellness Chatbot Training
# You feed it these sentences:
# - “Push-ups strengthen chest and triceps”
# - “Yoga improves flexibility and balance”
# - “Apples are rich in fiber”
# - “Cardio exercises boost heart health”
# 👉 In this case, the chatbot learns to distinguish between exercise facts, nutrition facts, and general wellness statements, so it can tailor its responses — e.g., suggesting workouts, offering diet tips, or explaining health benefits.
