import numpy as np
from sklearn.metrics.pairwise import cosine_similarity

# Patient symptom vector
# fever, cough, fatigue, headache
patient_vector = np.array([[0.2, 0.8, -0.3, 0.5]])

# Condition symptom profile vector
condition_vector = np.array([[0.1, 0.9, -0.2, 0.4]])

# Calculate cosine similarity
similarity = cosine_similarity(patient_vector, condition_vector)

print("Patient Symptom Vector:", patient_vector)
print("Condition Symptom Vector:", condition_vector)
print("Cosine Similarity Score:", similarity[0][0])

# Interpretation
if similarity[0][0] > 0.8:
    print("High similarity: Symptoms strongly match the condition.")
elif similarity[0][0] > 0.5:
    print("Moderate similarity: Possible condition match.")
else:
    print("Low similarity: Symptoms do not strongly match.")

